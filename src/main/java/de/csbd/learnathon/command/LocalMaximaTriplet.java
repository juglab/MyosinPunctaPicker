package de.csbd.learnathon.command;

public class LocalMaximaTriplet {
	
	int x;
	int y;
	float v;
	
	
	public LocalMaximaTriplet( int x, int y, float v ) {
		this.x = x;
		this.y = y;
		this.v = v;
	}
	
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	
	public float getV() {
		return v;
	}


}
